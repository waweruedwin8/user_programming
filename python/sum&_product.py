# a programme that takes user input of e numbers and gives their sum and product
number1 = int(input("Enter the first no: "))
number2 = int(input("Enter the second no: "))
sum = number1 + number2
product = number1 * number2
mean = sum / 2

print(f" the sum of numbers: {sum}")
print(f" the product of numbers: {product}")
print(f" the mean  of the numbers: {mean}")